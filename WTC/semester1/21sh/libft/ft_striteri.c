#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	cnt;

	cnt = 0;
	if (s != 0)
	{
		while (*s != 0)
		{
			f(cnt, s);
			cnt++;
			s++;
		}
	}
}
